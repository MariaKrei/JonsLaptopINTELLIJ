package Class1;

public class elseIfConditions
{
    public static void main(String[] args) {
        int day=4;
        if(day==1) {
            System.out.println("Monday");
        }else if(day==2) {
            System.out.println("Tuesday");
        }else if(day==3) {
            System.out.println("Wednesday");
        }else if(day==4) {
            System.out.println("Tursday");
        }else{
            System.out.println("Wrong Day");
        }
    }
}
