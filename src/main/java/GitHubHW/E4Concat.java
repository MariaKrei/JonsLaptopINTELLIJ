package GitHubHW;

public class E4Concat{

    public static void main(String[] args) {
        String fName= "John";
        String lName = "Kreilich";
        System.out.println(fName+lName);
        System.out.println(fName+" "+lName);

        String name="James";
        int age=10;
        String address="House no 123";
        System.out.println(name+age+address);
        System.out.println(name+" "+age+" "+address);

    }
}
