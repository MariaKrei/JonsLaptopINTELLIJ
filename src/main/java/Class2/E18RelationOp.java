package Class2;

public class E18RelationOp {
    public static void main(String[] args) {
        int num1=10;
        int num2=20;
        System.out.println(num1>num2);
        System.out.println(num1<num2 );
        //num1 is equal num 2
        System.out.println(num1==num2);
        //num1 is not equal num2
        //!= not equal
        //== equal
        System.out.println(num1!=num2);
        //
    }
}
