package Review4;

public class WhileLoop {
    public static void main(String[] args) {
        //while - we use when we dont know in advance
                //how many times we want to repeat block of code
        //i want to say hello 4 times
         int i=1;
         while (i<=4){
             System.out.println("Hello");
             i++;
         }
    }
}
