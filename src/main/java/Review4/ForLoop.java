package Review4;

public class ForLoop {
    //for loop is used when we know in advance
    //how many times we want to repeat block of code
    //say hello 4 times


    public static void main(String[] args) {
        for (int i=0; i<4; i++){
            System.out.println("Hello");
            //numbers 10 till 2

        }
        for (int j=10; j<=2; j--){
            System.out.print(j+" ");
        }
    }}
